package variaveisEtipos;

public class conversaoDeTipos {
    public static void main(String[] args){
        double valorDouble = 19.5;
        int valorInt = (int) valorDouble;

        System.out.println("--------------------");
        System.out.println("Transformar Double em Int");
        System.out.println("--------------------");



        System.out.println("O valor em double é : " + valorDouble);
        System.out.println("------------------------------------------");
        System.out.println("O valor inteiro  é :" + valorInt);
    }
}
